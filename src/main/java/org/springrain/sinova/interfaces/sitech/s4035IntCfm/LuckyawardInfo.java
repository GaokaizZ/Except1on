package org.springrain.sinova.interfaces.sitech.s4035IntCfm;

public class LuckyawardInfo {
	private String awardFlag;
	private String awardRank;
	private String prizeNo;
	private String checkCode;
	private String position;
	private String acceptType;
	public String getAwardFlag() {
		return awardFlag;
	}
	public void setAwardFlag(String awardFlag) {
		this.awardFlag = awardFlag;
	}
	public String getAwardRank() {
		return awardRank;
	}
	public void setAwardRank(String awardRank) {
		this.awardRank = awardRank;
	}
	public String getPrizeNo() {
		return prizeNo;
	}
	public void setPrizeNo(String prizeNo) {
		this.prizeNo = prizeNo;
	}
	public String getCheckCode() {
		return checkCode;
	}
	public void setCheckCode(String checkCode) {
		this.checkCode = checkCode;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getAcceptType() {
		return acceptType;
	}
	public void setAcceptType(String acceptType) {
		this.acceptType = acceptType;
	}
	
}
